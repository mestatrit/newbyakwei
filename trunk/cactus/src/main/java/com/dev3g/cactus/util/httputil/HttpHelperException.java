package com.dev3g.cactus.util.httputil;

public class HttpHelperException extends Exception {

	private static final long serialVersionUID = -8613187035280346764L;

	public HttpHelperException() {
		super();
	}

	public HttpHelperException(String message, Throwable cause) {
		super(message, cause);
	}

	public HttpHelperException(String message) {
		super(message);
	}

	public HttpHelperException(Throwable cause) {
		super(cause);
	}
}
